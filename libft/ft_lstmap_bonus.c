#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_lst;
	t_list	*node;
	void	*f_content;

	new_lst = NULL;
	while (lst)
	{
		f_content = f(lst->content);
		node = ft_lstnew(f_content);
		if (!node)
		{
			del(f_content);
			ft_lstclear(&new_lst, del);
			return (NULL);
		}
		ft_lstadd_back(&new_lst, node);
		lst = lst->next;
	}
	return (new_lst);
}
