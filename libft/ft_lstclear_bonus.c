#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*node;

	while (*lst)
	{
		del((*lst)->content);
		node = (*lst)->next;
		free(*lst);
		*lst = node;
	}
}
