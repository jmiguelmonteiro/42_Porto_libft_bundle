#include "libft.h"

int	ft_atoi(const char *nptr)
{
	unsigned int	value;
	int				signal;

	value = 0;
	signal = 1;
	while (*nptr == ' ' || (*nptr >= '\t' && *nptr <= '\r'))
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			signal = -1;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
		value = value * 10 + *nptr++ - '0';
	return (signal * value);
}
